
print(11 / 2)           # dělení
print(10 / 2)           # vždy vrací float
print(11 // 2)          # celočíselné dělení
print(-11 // 2)         # záporná čísla zaokrouhlena dolů
print(11.0 // 2)        # je float (!)
print(11 ** 2)          # umocňování
print(11 % 2)           # zbytek po celočíselném dělení